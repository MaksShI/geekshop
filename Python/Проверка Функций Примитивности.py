def isPrime():
    num = int(input("Input yours number: "))
    if num <= 3:
        return print()
    else:
        return num


mass = []
min_num = isPrime()
i = 1
while i != min_num:
    if min_num == print():
        break
    sad = min_num / i
    if sad == round(sad):
        mass.append(sad)
    i += 1
    print(mass)
if len(mass) > 1:
    print("Сложное")
else:
    print("Простое")
